#include <unistd.h>

int		main(int argc, char **argv)
{
	int		found;

	found = 0;
	if (argc == 2)
	{
		while (*argv[1])
		{
			if (*argv[1] == 'a')
				found = 1;
			argv[1]++;
		}
		if (found == 1)
			write(1, "a", 1);
		write(1, "\n", 1);
	}
	else
	{
		write(1, "a", 1);
		write(1, "\n", 1);
	}
	return (0);
}
