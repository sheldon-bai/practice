int		max(int *tab, unsigned int len)
{
	unsigned int	i;
	int				max;

	if (tab == 0 || len == 0)
		return (1);
	i = 0;
	max = tab[0];
	while (++i < len)
	{
		if (max < tab[i])
			max = tab[i];
	}
	return (max);
}
